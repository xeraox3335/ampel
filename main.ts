input.onButtonEvent(Button.A, input.buttonEventClick(), function () {
    basic.setLedColor(0xff0000)
    basic.pause(1000)
    basic.setLedColor(0xffff00)
    basic.pause(700)
    basic.setLedColor(0x00ff00)
    basic.pause(5000)
    basic.setLedColor(0xffff00)
    basic.pause(500)
    basic.setLedColor(0xff0000)
})
